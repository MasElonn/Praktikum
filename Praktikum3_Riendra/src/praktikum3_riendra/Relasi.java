public class Relasi {
    public static void main(String[] args) {
        int a = 6, b = 3;
        
        System.out.println(a > b); // Lebih dari
        
        System.out.println(a < b); // Kurang dari
        
        System.out.println(a == b); // Sama Dengan
        
        System.out.println(a != b); // Apakah tidak sama
        
        System.out.println(a >= b); // Lebih dari atau sama dengan
        
        System.out.println(a <= b); // Kurang dari atau sama dengan
        
        
        
    }
}
