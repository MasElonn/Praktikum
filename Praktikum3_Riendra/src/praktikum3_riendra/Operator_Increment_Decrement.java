public class Operator_Increment_Decrement {
    public static void main(String[] args) {
        int a = 5, b = 5, c = 5, d = 5;
        
        // Increment
        a++;
        System.out.println(a);
        
        ++b;
        System.out.println(b);
        
        // Decrement
        c--;
        System.out.println(c);
        
        --d;
        System.out.println(c);
        
    }
    
}
