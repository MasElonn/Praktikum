public class Logika {
    public static void main(String[] args) {
        int a = 5;
        
        System.out.println(a > 7 && a < 11); // AND Operator
        
        System.out.println(a > 7 || a < 11); // OR Operator
        
        System.out.println(!(a > 7 && a < 11)); // NOT Operator
    }
    
}
